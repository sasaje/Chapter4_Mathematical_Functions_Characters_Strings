public class MinMaxMethods {
    public static void main(String[] args) {

        double minMath = Math.min(3,2);
        System.out.println(minMath); // prints 2

        double maxMath = Math.min(3,2);
        System.out.println(maxMath); // prints 3

        int abs = Math.abs(-2); // return the positive result of the number eg. -2 = 2
        System.out.println(abs); // prints 2

        double absDouble = Math.abs(-2.1); // return the positive result of the number eg. -2 = 2
        System.out.println(absDouble); // prints 2.1

    }
}
